#ifndef SIREN_H
#define SIREN_H

/*
 * This function produces the 1st siren sound with ON/OFF led transition.
 */
void one();

/*
 * This function produces different transition on 1st siren.
 */
void oneA();

/*
 * This function produces the 2nd siren sound with progressive led transition.
 */
void two();

/*
 * This function produces different transition on 2nd siren.
 */
void twoA();

/*
 * This function produces the 3rd siren (AMBULANCE) sound with led transition.
 */
void three();

/*
 * This function produces different transition on 3rd siren.
 */
void threeA();

/*
 * This function produces the 4th siren (POLICE) sound with led transition.
 */
void four();

#endif
